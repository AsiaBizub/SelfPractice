package Practice_Tasks;

public class SwapVariables {
    public static void main(String[] args) {
        int a = 10;
        a = 15;
        int b = 15;
        b= 10 ;




        System.out.println("a ="+a);
        System.out.println("b ="+b);
        System.out.println("===============");

        int a1 = 10;
        a1 +=5; // a1=a1+5
        int b1 = 15;
        b1 -=5; // b1=12-5

        System.out.println("a is "+a1);
        System.out.println("b is "+b1);




    }
}
